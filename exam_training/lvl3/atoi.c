int	ft_atoi(const char *str)
{
	int nb;
	int	i;
	int	neg;

	i = 0;
	neg = 0;
	nb = 0;
	while (str[i] == ' ' || (str[i] >= '\t' && str[i] <= '\r'))
		i++;
	if (str[i] == '-')
		neg = 1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	if (str[i] >= '0' && str[i] <= '9')
		nb = str[i] + '0';
	return (0);
}